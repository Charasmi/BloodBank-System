const mongoose = require("mongoose");
const inventoryModel = require("../models/inventoryModel");
const userModel = require("../models/userModel");

// CREATE INVENTORY
const createInventoryController = async (req, res) => {
  try {
    const { email, inventoryType, bloodGroup, quantity } = req.body;

    // Check target user (donar or hospital) by email
    const targetUser = await userModel.findOne({ email });
    if (!targetUser) {
      return res.status(404).send({
        success: false,
        message: "User Not Found",
      });
    }

    // Organisation performing this action
    const organisationId = req.user._id;

    // If inventory is being issued (hospital requesting blood)
    if (inventoryType === "out") {
      // Calculate total IN
      const totalIn = await inventoryModel.aggregate([
        {
          $match: {
            organisation: organisationId,
            inventoryType: "in",
            bloodGroup,
          },
        },
        {
          $group: {
            _id: "$bloodGroup",
            total: { $sum: "$quantity" },
          },
        },
      ]);

      const inQty = totalIn[0]?.total || 0;

      // Calculate total OUT
      const totalOut = await inventoryModel.aggregate([
        {
          $match: {
            organisation: organisationId,
            inventoryType: "out",
            bloodGroup,
          },
        },
        {
          $group: {
            _id: "$bloodGroup",
            total: { $sum: "$quantity" },
          },
        },
      ]);

      const outQty = totalOut[0]?.total || 0;

      const availableQty = inQty - outQty;

      if (availableQty < quantity) {
        return res.status(400).send({
          success: false,
          message: `Only ${availableQty}ML of ${bloodGroup.toUpperCase()} is available`,
        });
      }

      // Set relations
      req.body.hospital = targetUser._id;       // hospital receiving blood
      req.body.organisation = organisationId;   // org fulfilling the request
    } else {
      // inventoryType === "in"
      req.body.donar = targetUser._id;          // donar donating blood
      req.body.organisation = organisationId;   // org collecting the donation
    }

    const inventory = new inventoryModel(req.body);
    await inventory.save();

    return res.status(201).send({
      success: true,
      message: "New Blood Record Added",
    });

  } catch (error) {
    console.log(error);
    return res.status(500).send({
      success: false,
      message: "Error In Create Inventory API",
      error,
    });
  }
};


